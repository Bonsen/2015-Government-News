package com.news.content.bean;

import java.io.Serializable;

public class NewsContentBean implements Serializable{

	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public NewsContentBean(){}
	
	
	public NewsContentBean(String title, String time, String content,
			int status, String cColumn, String pColumn, String author) {
		super();
		this.time = time;
		this.content = content;
		this.title = title;
		this.status = status;
		this.cColumn = cColumn;
		this.pColumn = pColumn;
		this.author = author;
	}

	public String time;
	public String content;
	public String title;

	public int id;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int status;

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public String author;
	public String cColumn;

	public String getcColumn() {
		return cColumn;
	}

	public void setcColumn(String cColumn) {
		this.cColumn = cColumn;
	}

	public String getpColumn() {
		return pColumn;
	}

	public void setpColumn(String pColumn) {
		this.pColumn = pColumn;
	}

	public String pColumn;

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

}
